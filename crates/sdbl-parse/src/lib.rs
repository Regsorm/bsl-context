//! Разбор подмножества языка запросов 1С.
//!
//! Крейт отвечает на структурные вопросы, которые нужны правилам оптимальности:
//! откуда запрос читает данные, как соединяет источники, кладёт ли результат во
//! временную таблицу и индексирует ли её. Выражения, агрегаты и предикаты он
//! глотает целиком, сохраняя из них только упомянутые поля и наличие `ИЛИ`.
//!
//! Готовой грамматики SDBL для Rust не существует: у `1c-syntax` она написана
//! под ANTLR, а работающего порта ANTLR на Rust нет. Отсюда разбор вручную —
//! и сознательно неполный.
//!
//! Зависимостей нет, как и у `bsl-parse`: разбор текста не должен тянуть за
//! собой ни платформенный контекст, ни доступ к конфигурации.
//!
//! ```
//! let package = sdbl_parse::parse("ВЫБРАТЬ Т.Ссылка ИЗ Справочник.Товары КАК Т").unwrap();
//! assert_eq!(package.queries.len(), 1);
//! ```

pub mod ast;
pub mod lexer;
pub mod parser;

pub use ast::{Condition, Field, Join, JoinKind, MetaTable, Named, Package, Query, Source, Table};
pub use parser::{parse, ParseError};

/// Имена виртуальных таблиц регистров.
///
/// Нужны, чтобы отличить `РегистрНакопления.Х.Остатки` (виртуальная таблица) от
/// `Документ.Х.Товары` (табличная часть): по третьему сегменту имени они
/// неразличимы, а правила про физические таблицы и отборы опираются именно на
/// это различие.
const VIRTUAL_TABLES: &[&str] = &[
    "ОСТАТКИ",
    "ОБОРОТЫ",
    "ОСТАТКИИОБОРОТЫ",
    "ОБОРОТЫДТКТ",
    "СРЕЗПОСЛЕДНИХ",
    "СРЕЗПЕРВЫХ",
    "ДВИЖЕНИЯССУБКОНТО",
    "ОСТАТКИИОБОРОТЫДТКТ",
    "СУБКОНТО",
    "ФАКТИЧЕСКИЙПЕРИОДДЕЙСТВИЯ",
    "ДАННЫЕГРАФИКА",
    "БАЗАНАЧИСЛЕНО",
    "BALANCE",
    "TURNOVERS",
    "BALANCEANDTURNOVERS",
    "DRCRTURNOVERS",
    "SLICELAST",
    "SLICEFIRST",
    "RECORDSWITHEXTDIMENSIONS",
    "EXTDIMENSIONS",
    "ACTUALACTIONPERIOD",
    "SCHEDULEDATA",
    "BASE",
];

/// Является ли третий сегмент имени таблицы виртуальной таблицей регистра.
pub fn is_virtual_table(name: &str) -> bool {
    let up = name.to_uppercase();
    VIRTUAL_TABLES.contains(&up.as_str())
}

/// Читает ли таблица регистр — физически или через виртуальную таблицу.
pub fn is_register(kind: &str) -> bool {
    let up = kind.to_uppercase();
    matches!(
        up.as_str(),
        "РЕГИСТРНАКОПЛЕНИЯ"
            | "РЕГИСТРСВЕДЕНИЙ"
            | "РЕГИСТРБУХГАЛТЕРИИ"
            | "РЕГИСТРРАСЧЕТА"
            | "ACCUMULATIONREGISTER"
            | "INFORMATIONREGISTER"
            | "ACCOUNTINGREGISTER"
            | "CALCULATIONREGISTER"
    )
}
