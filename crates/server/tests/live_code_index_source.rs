//! Живая проверка источника имён поверх `code-index` по сети.
//!
//! Этот набор существует потому, что два дефекта прошли и юнит-тесты, и регресс
//! по корпусу, и `probe_levels` — их поймал только запрос к ЖИВОМУ `code-index`.
//! Оба — дефекты ЭТОГО источника: `code-index` про обрезки сообщает честно, а
//! источник читал не те поля.
//!
//! 1. **Страж размера ответа.** `bsl_sql` входит в `DEFAULT_CAP_TOOLS`: ответ
//!    режется по `[mcp].max_response_bytes` (48 000 байт). Список общих модулей
//!    УТ весит ~170 КБ, приходило 386 строк из 3091 — по алфавиту до «И», и
//!    реальный `ОбщегоНазначения` объявлялся несуществующим. Сервер сообщал об
//!    этом через `rows_total`/`rows_truncated`/`response_truncated` + подсказку;
//!    источник же смотрел на `truncated`, а оно про ДРУГОЙ лимит и правдиво
//!    отвечало `false`.
//! 2. **Сессионный дедуп.** `serve` опускает строки, уже отданные в этой сессии,
//!    явно помечая это полем `rows_elided_already_delivered`. Имена пересекаются
//!    между коллекциями (`Закупки` — и общий модуль, и регистр накопления),
//!    поэтому `SELECT name` терял их во втором запросе.
//!
//! Оба дефекта невидимы для `LiteSource` (корпусный регресс идёт на нём) и для
//! стабов. Единственная защита — спросить живой сервер про РЕАЛЬНЫЕ имена.
//!
//! `#[ignore]`: нужен запущенный `code-index` с репозиторием `ut`.
//!
//! ```pwsh
//! cargo test -p bsl-context-server --test live_code_index_source -- --ignored --nocapture
//! ```

use symbol_source::CodeIndexMcpSource;

use bsl_validator::SymbolSource;

const CODE_INDEX_URL: &str = "http://127.0.0.1:8011/mcp";
const REPO: &str = "ut";

fn source() -> Option<CodeIndexMcpSource> {
    match CodeIndexMcpSource::new(CODE_INDEX_URL.to_string(), REPO.to_string(), 15_000) {
        Ok(src) => Some(src),
        Err(e) => {
            eprintln!("skip: code-index недоступен ({CODE_INDEX_URL}, repo={REPO}): {e:#}");
            None
        }
    }
}

/// Набор общих модулей приходит ЦЕЛИКОМ, а не первой страницей.
///
/// `ОбщегоНазначения` выбран не случайно: он сортируется после «И», то есть
/// именно он терялся при обрезке стражем размера. `Закупки` сортируется до неё
/// и оставался — поэтому проверять надо оба, иначе обрезка выглядит рабочей.
#[test]
#[ignore]
fn common_modules_set_is_complete_not_first_page() {
    let Some(src) = source() else { return };

    for name in ["общегоназначения", "закупки", "управлениедоступом"] {
        assert_eq!(
            src.object_exists("CommonModules", name),
            Some(true),
            "общий модуль '{name}' есть в УТ, но источник его не видит — \
             набор пришёл неполным (страж размера ответа code-index?)"
        );
    }
    assert_eq!(
        src.object_exists("CommonModules", "управлениепрогрессом"),
        Some(false),
        "выдуманного модуля в конфигурации нет — источник обязан это сказать"
    );
}

/// Имя, живущее в ДВУХ коллекциях, видно в обеих.
///
/// Проверяет сессионный дедуп `code-index`: он опускает повторно отданные
/// строки. Порядок вызовов важен — сначала коллекция, где имя встретится
/// первым, потом вторая; при запросе по `name` вторая недосчиталась бы строки.
#[test]
#[ignore]
fn name_shared_between_collections_visible_in_both() {
    let Some(src) = source() else { return };

    // `Закупки` в УТ — и общий модуль, и регистр накопления.
    assert_eq!(src.object_exists("CommonModules", "закупки"), Some(true));
    assert_eq!(
        src.object_exists("AccumulationRegisters", "закупки"),
        Some(true),
        "регистр накопления 'Закупки' существует; если его не видно — \
         строку опустил сессионный дедуп code-index"
    );

    // `ПодарочныеСертификаты` — и регистр накопления, и справочник.
    assert_eq!(
        src.object_exists("AccumulationRegisters", "подарочныесертификаты"),
        Some(true)
    );
    assert_eq!(
        src.object_exists("Catalogs", "подарочныесертификаты"),
        Some(true),
        "справочник 'ПодарочныеСертификаты' существует; если его не видно — \
         строку опустил сессионный дедуп code-index"
    );
}

/// Экспортные переменные модуля приложения читаются с живого сервера.
/// Без них правило про общий модуль обязано молчать целиком, то есть их
/// отсутствие тихо выключило бы половину задачи.
#[test]
#[ignore]
fn application_module_variables_are_read() {
    let Some(src) = source() else { return };

    let vars = src
        .global_variables()
        .expect("источник обязан уметь отвечать про глобальные переменные");
    assert!(
        vars.contains("параметрыприложения"),
        "в модуле приложения УТ объявлена `Перем ПараметрыПриложения Экспорт;`, \
         но источник её не видит: {vars:?}"
    );
}
