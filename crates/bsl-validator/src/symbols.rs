//! Внешний источник имён: методы, объявленные в ДРУГИХ модулях конфигурации.
//!
//! Валидатор видит только присланный текст. Отсюда ложные `UndeclaredMethod`
//! на процедурах глобальных общих модулей (зовутся без префикса откуда угодно)
//! и на модулях обычных форм внешних обработок (видят экспортные методы модуля
//! объекта-владельца). Источник закрывает ровно эти два случая и, кроме того,
//! позволяет понизить уверенность находки, если имя вообще где-то объявлено.

use std::collections::HashSet;

pub trait SymbolSource: Send + Sync {
    /// Экспортный метод ГЛОБАЛЬНОГО общего модуля — такой зовут без префикса
    /// откуда угодно, находкой он быть не может.
    /// Источники, которые этого не знают (база code-index, MCP), возвращают `false`.
    fn is_global_export(&self, _name_lower: &str) -> bool {
        false
    }

    /// Метод с таким именем объявлен ГДЕ-НИБУДЬ в конфигурации.
    /// Не основание молчать: вызов может быть недоступен отсюда по правилам
    /// видимости. Основание понизить уверенность находки.
    fn method_exists(&self, name_lower: &str) -> bool;

    /// Экспортные методы модуля объекта-владельца — для модуля обычной формы
    /// внешней обработки. `None` — источник не знает про такой модуль.
    fn owner_exports(&self, _module_path: &str) -> Option<HashSet<String>> {
        None
    }

    /// Человекочитаемое имя источника (для /health и логов).
    fn describe(&self) -> String;
}
