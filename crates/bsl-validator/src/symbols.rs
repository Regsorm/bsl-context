//! Внешний источник имён: методы, объявленные в ДРУГИХ модулях конфигурации.
//!
//! Валидатор видит только присланный текст. Отсюда ложные `UndeclaredMethod`
//! на процедурах глобальных общих модулей (зовутся без префикса откуда угодно)
//! и на модулях обычных форм внешних обработок (видят экспортные методы модуля
//! объекта-владельца). Источник закрывает ровно эти два случая и, кроме того,
//! позволяет понизить уверенность находки, если имя вообще где-то объявлено.

use std::collections::HashSet;

pub trait SymbolSource: Send + Sync {
    /// Экспортный метод ГЛОБАЛЬНОГО общего модуля — такой зовут без префикса
    /// откуда угодно, находкой он быть не может.
    /// Источники, которые этого не знают (база code-index, MCP), возвращают `false`.
    fn is_global_export(&self, _name_lower: &str) -> bool {
        false
    }

    /// Метод с таким именем объявлен ГДЕ-НИБУДЬ в конфигурации.
    /// Не основание молчать: вызов может быть недоступен отсюда по правилам
    /// видимости. Основание понизить уверенность находки.
    fn method_exists(&self, name_lower: &str) -> bool;

    /// Экспортные методы модуля объекта-владельца — для модуля обычной формы
    /// внешней обработки. `None` — источник не знает про такой модуль.
    fn owner_exports(&self, _module_path: &str) -> Option<HashSet<String>> {
        None
    }

    /// Объект конфигурации с таким именем существует в указанной коллекции.
    /// `collection` — во множественном числе, как каталог выгрузки:
    /// "CommonModules", "Catalogs", "Documents", ...
    /// `name_lower` — имя в нижнем регистре.
    /// `None` — источник не умеет ответить (тогда валидатор обязан молчать).
    fn object_exists(&self, _collection: &str, _name_lower: &str) -> Option<bool> {
        None
    }

    /// Все имена объектов коллекции (в ИСХОДНОМ регистре) — для подсказки
    /// «возможно, вы имели в виду». `None` — подсказки не будет.
    fn collection_names(&self, _collection: &str) -> Option<HashSet<String>> {
        None
    }

    /// Экспортные переменные модулей приложения (`Перем ПараметрыПриложения
    /// Экспорт;` в `ManagedApplicationModule` и подобных), в нижнем регистре.
    ///
    /// Такое имя видно БЕЗ префикса из любого клиентского модуля, поэтому для
    /// проверки это глобальное имя наравне с экспортным методом глобального
    /// общего модуля. Общий модуль переменных иметь не может — других
    /// источников таких имён нет.
    ///
    /// `None` — источник не умеет ответить. Проверка «неизвестный общий модуль»
    /// обязана в этом случае молчать ЦЕЛИКОМ: не зная этих имён, она принимает
    /// `ПараметрыПриложения.Вставить(...)` за обращение к несуществующему
    /// модулю (замер на УТ: 123 ложные находки на одном лишь этом имени).
    fn global_variables(&self) -> Option<HashSet<String>> {
        None
    }

    /// Источник в рабочем состоянии. Сетевой источник может «сломаться» на ходу
    /// (code-index упал, репозиторий пропал) — и тогда он отвечает «имени нет» на
    /// всё. Молча принимать это за истину нельзя: валидатор выдал бы находку
    /// «метод не объявлен» на каждый вызов. Локальные источники всегда исправны.
    fn is_healthy(&self) -> bool {
        true
    }

    /// Человекочитаемое имя источника (для /health и логов).
    fn describe(&self) -> String;
}
